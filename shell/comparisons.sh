#!/bin/bash

VAL_X=5
VAL_Y=3

$VAL_X -eq $VAL_Y # returns true if the values are equal
$VAL_X -ge $VAL_Y # returns true if val_x is greater than or equal to val_y
$VAL_X -gt $VAL_Y # returns true if val_x is greater than val_y
$VAL_X -le $VAL_Y # returns true if val_x is less than or equal to val_y
$VAL_X -lt $VAL_Y # returns true if val_x os less than val_y
$VAL_X -ne $VAL_Y # returns true if the values are not equal
