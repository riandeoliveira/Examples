// Java é uma linguagem fortemente tipada, onde variáveis precisam ser declaradas com seus tipos de valores.
package documentation;

public class Variaveis {
    public static void main(String[] args) throws Exception {
        // Variável, com seu tipo, nome e valor:
        int n = 10;

        // Variável com typecast:
        int a = (int) 5;

        // Variável inalterável, equivalente a const do JS:
        final int num = 2;
    }
}
