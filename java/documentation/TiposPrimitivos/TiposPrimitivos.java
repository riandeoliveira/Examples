package documentation.TiposPrimitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        // Tipos Primitivos de Dados: 8 tipos

            // Inteiros: 4 tipos
            byte n1 = 63; // 8 bits: de -128 a 127
            short n2 = -16384; // 16 bits: de -32768 a 32767
            int n3 = 1073741823; // 32 bits: de -2147483648 a 2147483647
            long n4 = -4611686018427388L; // 64 bits: de -9223372036854775808 a 9223372036854775807

            // Pontos Flutuantes: 2 tipos
            float n6 = 6.09f; // 32 bits: de 1.4E-45 a 3.4028235E38
            double n7 = 11.44; // 64 bits: de 4.9E-324 a 1.7976931348623157E308

            // Caractere: 1 tipo
            char letra = 'A';
            char letraAcentuada = '\u00DA'; // Ãš

            // Booleanos: 1 tipo
            boolean verdadeiro = true;
            boolean falso = false;
    }
}
