package documentation;

public class ClasseMath {
    public static void main(String[] args) {
        // A Classe Math é muio usada em operações matemáticas.

        double pi = Math.PI; // Valor de pi: 3,141593
        double pow = Math.pow(5, 2); // Potência: 25
        double sqrt = Math.sqrt(25); // Raiz quadrada: 5
        double cbrt = Math.cbrt(27); // Raiz cúbica: 3
        double abs = Math.abs(-10); // Valor absoluto: 10
        double floor = Math.floor(3.9); // Arredondamento para baixo: 3
        double ceil = Math.ceil(4.2); // Arredondamento para cima: 5
        double round = Math.round(5.6); // Arredondamento aritmético: 6
        double random = Math.random(); // Número aleatório entre: 0 e 1
    }
}
