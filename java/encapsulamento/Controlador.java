// Interface Controlador
public interface Controlador {
    public abstract void liga();

    public abstract void desliga();

    public abstract void abreMenu();

    public abstract void fechaMenu();

    public abstract void aumentaVolume();

    public abstract void diminuiVolume();

    public abstract void ligaMudo();

    public abstract void desligaMudo();

    public abstract void play();

    public abstract void pause();
}